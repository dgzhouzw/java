package org.example.java.foundational.control.forstatement;

/**
 * 打印输出1-100的整数
 *
 * @author zzw dgzzw1@163.com
 * @version 2022/12/28 17:05
 * @since JDK17
 */
public class For1To100 {

	public static void main(String[] args) {

		// 需求：往控制台打印输出1-100的整数

		for (int i = 1; i <= 100; i++) {
			System.out.print(i + " ");
		}
	}
}

package org.example.java.foundational.control.forstatement;

/**
 * 打印输出100-1的整数
 *
 * @author zzw dgzzw1@163.com
 * @version 2022/12/28 17:06
 * @since JDK17
 */
public class For100To1 {

	public static void main(String[] args) {

		for (int i = 100; i >= 1; i--) {
			System.out.print(i + " ");
		}
	}
}
